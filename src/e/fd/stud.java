    private void wte(String p) {
        String path = p;
        XSSFWorkbook wb = new XSSFWorkbook();
        XSSFSheet ws = wb.createSheet();

        TreeMap<String, Object[]> data = new TreeMap<>();
        data.put("0", new Object[]{stud_tab.getColumnName(0), stud_tab.getColumnName(1)});
        String aaa = null;
        for (int i = 0; i < stud_tab.getRowCount(); i++) {
            int z = i + 1;
            aaa = aaa + z;
            data.put(aaa, new Object[]{stud_tab.getValueAt(i, 0), stud_tab.getValueAt(i, 1), stud_tab.getValueAt(i, 2)});
        }
        Set<String> ids = data.keySet();
        XSSFRow row;
        int rowID = 0;
        for (String key : ids) {
            row = ws.createRow(rowID++);
            Object[] values = data.get(key);
            int cellID = 0;
            for (Object o : values) {
                XSSFCell cell = row.createCell(cellID++);
                cell.setCellValue(o.toString());
            }
        }
        try {
            FileOutputStream fos = new FileOutputStream(new File(path));
            wb.write(fos);
            fos.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        JOptionPane.showMessageDialog(null, "Successfully Save");
    }

