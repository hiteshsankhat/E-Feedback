    private void d_boxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_d_boxActionPerformed
        con1 = JavaConnectDB.ConnectDB();
        con2 = JavaConnectDB.ConnectDB();
        DefaultTableModel th_dtm = (DefaultTableModel) stud_tab.getModel();
        while (th_dtm.getRowCount() > 0) {
            for (int i = 0; i < th_dtm.getRowCount(); i++) {
                th_dtm.removeRow(i);
            }
        }
        branch = (String) b_box.getSelectedItem();
        semester = (String) s_box.getSelectedItem();
        div = (String) d_box.getSelectedItem();
        try {
            String sql = "select roll_id , name from student_info where branch=? and semester=? and division=?";
            pst = (OraclePreparedStatement) con1.prepareStatement(sql);
            pst.setString(1, branch);
            pst.setString(2, semester);
            pst.setString(3, div);
            rs = (OracleResultSet) pst.executeQuery();
            while (rs.next()) {
                roll_id = rs.getString("roll_id");
                name = rs.getString("Name");
                String sql2 = "select co_username from login_data where co_username is not null";
                pst2 = (OraclePreparedStatement) con2.prepareStatement(sql2);
                rs1 = (OracleResultSet) pst2.executeQuery();
                while (rs1.next()) {
                    String rn = rs1.getString("co_username");
                    if (roll_id.equals(rn)) {
                        roll = rn;
                        break;
                    }
                }
                if (roll_id.equals(roll)) {
                } else {
                    sem++;
                    DefaultTableModel model = (DefaultTableModel) stud_tab.getModel();
                    model.insertRow(stud_tab.getRowCount(), new Object[]{sem, roll_id, name, surname});
                }
            }
            sem = 0;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_d_boxActionPerformed

