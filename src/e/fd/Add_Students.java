    private void ADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ADDActionPerformed
        con1 = JavaConnectDB.ConnectDB();
        int row = stud_info.getRowCount();
        try {
            for (i = 0; i < row; i++) {
                branch = (String) stud_info.getValueAt(i, 1);
                semester = (String) stud_info.getValueAt(i, 2);
                div = (String) stud_info.getValueAt(i, 3);
                roll_id = (String) stud_info.getValueAt(i, 5);
                name = (String) stud_info.getValueAt(i, 4);
                password = (String) stud_info.getValueAt(i, 6);
                String sql = "insert into student_info values(?,?,?,?,?,?)";
                pst = (OraclePreparedStatement) con1.prepareStatement(sql);
                pst.setString(1, roll_id.toUpperCase());
                pst.setString(5, branch.toUpperCase());
                pst.setString(6, div.toUpperCase());
                pst.setString(3, semester);
                pst.setString(2, name);
                pst.setString(4, password.toUpperCase());
                rs = (OracleResultSet) pst.executeQuery();
            }
            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "Sucessfully added");
                DefaultTableModel th_dtm = (DefaultTableModel) stud_info.getModel();
                while (th_dtm.getRowCount() > 0) {
                    for (int j = 0; j < th_dtm.getRowCount(); j++) {
                        th_dtm.removeRow(j);
                    }
                }
            }
        } catch (NullPointerException n) {
            JOptionPane.showMessageDialog(null, n);
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        //ADD.setEnabled(false);
        Number.setText("");
        // TODO add your handling code here:
    }//GEN-LAST:event_ADDActionPerformed

