    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        DefaultTableModel th_dtm = (DefaultTableModel) stud_info.getModel();
        while (th_dtm.getRowCount() > 0) {
            for (int i = 0; i < th_dtm.getRowCount(); i++) {
                th_dtm.removeRow(i);
            }
        }
        String Excelpath = null;
        try {
            FileInputStream fs = null;
            JFileChooser jf = new JFileChooser();
            jf.setDialogTitle("Save Excel File");
            int res = jf.showSaveDialog(null);
            if (res == JFileChooser.APPROVE_OPTION) {
                Excelpath = jf.getSelectedFile().getAbsolutePath();
            }
            try {
                fs = new FileInputStream(Excelpath);
            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
            XSSFWorkbook wb = new XSSFWorkbook(fs);
            String[] strs = new String[wb.getNumberOfSheets()];
            //get all sheet names from selected workbook
            for (int i = 0; i < strs.length; i++) {
                strs[i] = wb.getSheetName(i);
            }
            JFrame frame = new JFrame("Input Dialog");
            int index = 0;
            String selectedsheet = (String) JOptionPane.showInputDialog(frame, "Which worksheet you want to import ?", "Select Worksheet", JOptionPane.QUESTION_MESSAGE, null, strs, strs[0]);
            if (selectedsheet != null) {
                for (int i = 0; i < strs.length; i++) {
                    if (wb.getSheetName(i).equalsIgnoreCase(selectedsheet)) {
                        index = i;
                    }
                }
            }
            XSSFSheet sh = wb.getSheetAt(index);
            FormulaEvaluator fe = (FormulaEvaluator) wb.getCreationHelper().createFormulaEvaluator();

            for (Row row : sh) {
                for (org.apache.poi.ss.usermodel.Cell cell : row) {
                    switch (fe.evaluateInCell(cell).getCellType()) {
                        case org.apache.poi.ss.usermodel.Cell.CELL_TYPE_NUMERIC:
                            int num = (int) cell.getNumericCellValue();
                            //v.addElement(num);
                            v.add(num);
                            break;
                        case org.apache.poi.ss.usermodel.Cell.CELL_TYPE_STRING:
                            String n = cell.getStringCellValue();
                            //v.addElement(n);
                            v.add(n);
                            break;
                    }
                }

                if (!v.isEmpty()) {
                    coun = v.elementAt(0).toString();
                    cnt = Integer.toString(c);
                    if (coun.equals(cnt)) {
                        if (!v.isEmpty()) {
                            DefaultTableModel tabmod = (DefaultTableModel) stud_info.getModel();
                            tabmod.insertRow(stud_info.getRowCount(), new Object[]{v.elementAt(0).toString().toUpperCase(), v.elementAt(1).toString().toUpperCase(), v.elementAt(2).toString().toUpperCase(), v.elementAt(3).toString().toUpperCase(), v.elementAt(4).toString().toUpperCase(), v.elementAt(5).toString().toUpperCase(), v.elementAt(5).toString().toUpperCase()});
                            v.removeAllElements();
                            c = c + 1;
                        }
                    } else {
                        v.removeAllElements();
                    }
                }

            }
            c = 1;
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

